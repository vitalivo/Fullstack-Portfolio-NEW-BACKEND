import os
import django
import json
from django.core.management.base import BaseCommand

# –ù–∞—Å—Ç—Ä–æ–π–∫–∞ Django –¥–ª—è –ø—Ä–æ–¥–∞–∫—à–µ–Ω–∞
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'config.settings_production')
django.setup()

class Command(BaseCommand):
    help = 'Import initial data (safe for existing data)'

    def handle(self, *args, **options):
        """–ò–º–ø–æ—Ä—Ç –¥–∞–Ω–Ω—ã—Ö –≤ –ø—Ä–æ–¥–∞–∫—à–Ω –±–∞–∑—É —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –¥–∞–Ω–Ω—ã—Ö"""
        
        files_to_import = [
            ('data_export_core.json', None),  # –í—Å–µ–≥–¥–∞ –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º core –¥–∞–Ω–Ω—ã–µ
            ('data_export_skills.json', 'skills.Skill'),
            ('data_export_portfolio.json', 'portfolio.Project'), 
            ('data_export_certificates.json', 'certificates.Certificate'),
            ('data_export_blog.json', 'blog.BlogPost'),  # –ü—Ä–æ–≤–µ—Ä—è–µ–º –±–ª–æ–≥-–ø–æ—Å—Ç—ã
            ('data_export_contacts.json', 'contacts.Contact')
        ]
        
        for filename, model_check in files_to_import:
            if os.path.exists(filename):
                try:
                    should_import = True
                    if model_check:
                        app_label, model_name = model_check.split('.')
                        from django.apps import apps
                        try:
                            model_class = apps.get_model(app_label, model_name)
                            if model_class.objects.exists():
                                self.stdout.write(
                                    self.style.WARNING(f'‚ö†Ô∏è {model_check} data already exists, skipping {filename}')
                                )
                                should_import = False
                        except Exception as e:
                            self.stdout.write(
                                self.style.WARNING(f'‚ö†Ô∏è Could not check {model_check}: {e}')
                            )
                    
                    if should_import:
                        if filename == 'data_export_blog.json':
                            self.stdout.write(f"üì• Processing {filename} with author_id fix...")
                            self._import_blog_data_with_author_fix(filename)
                        else:
                            self.stdout.write(f"üì• Importing {filename}...")
                            from django.core.management import call_command
                            call_command('loaddata', filename)
                        
                        self.stdout.write(
                            self.style.SUCCESS(f"‚úÖ {filename} imported successfully")
                        )
                    
                except Exception as e:
                    self.stdout.write(
                        self.style.ERROR(f"‚ùå Error importing {filename}: {e}")
                    )
            else:
                self.stdout.write(
                    self.style.WARNING(f"‚ö†Ô∏è File {filename} not found")
                )

        self.stdout.write(self.style.SUCCESS("\nüéâ Import completed!"))

    def _import_blog_data_with_author_fix(self, filename):
        """–ò–º–ø–æ—Ä—Ç –±–ª–æ–≥ –¥–∞–Ω–Ω—ã—Ö —Å –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ–º author_id –Ω–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Å—É–ø–µ—Ä–ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
        from django.contrib.auth.models import User
        from blog.models import BlogPost, Category
        
        # –ù–∞—Ö–æ–¥–∏–º —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Å—É–ø–µ—Ä–ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        superuser = User.objects.filter(is_superuser=True).first()
        if not superuser:
            self.stdout.write(
                self.style.ERROR("‚ùå No superuser found for blog posts")
            )
            return
        
        # –ß–∏—Ç–∞–µ–º –∏ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º JSON —Ñ–∞–π–ª
        with open(filename, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        # –ò—Å–ø—Ä–∞–≤–ª—è–µ–º author_id –≤–æ –≤—Å–µ—Ö –∑–∞–ø–∏—Å—è—Ö –±–ª–æ–≥–∞
        for item in data:
            if item['model'] == 'blog.blogpost':
                item['fields']['author'] = superuser.id
                self.stdout.write(f"üîß Fixed author_id to {superuser.id} for post: {item['fields'].get('title_ru', 'Unknown')}")
        
        # –°–æ–∑–¥–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—ã–π —Ñ–∞–π–ª —Å –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–Ω—ã–º–∏ –¥–∞–Ω–Ω—ã–º–∏
        temp_filename = f"temp_{filename}"
        with open(temp_filename, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        
        try:
            # –ó–∞–≥—Ä—É–∂–∞–µ–º –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
            from django.core.management import call_command
            call_command('loaddata', temp_filename)
        finally:
            # –£–¥–∞–ª—è–µ–º –≤—Ä–µ–º–µ–Ω–Ω—ã–π —Ñ–∞–π–ª
            if os.path.exists(temp_filename):
                os.remove(temp_filename)

def import_data():
    """Legacy function for backward compatibility"""
    from django.core.management import call_command
    call_command('import_data')

if __name__ == "__main__":
    import_data()
